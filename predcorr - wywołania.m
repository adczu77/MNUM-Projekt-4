f1 = @(x1,x2) x2 + x1*(0.7-(x1)^2-(x2)^2);
f2 = @(x1,x2) -x1 + x2*(0.7-(x1)^2-(x2)^2);
[x1v, x2v, t] = predcorr(f1,f2,0,10,0,15,0.023);
[T, Y] = ode45('RozwODE', (0:0.001:15), [0,10]);
figure;
hold on;
plot(t,x1v(1:length(t)));
plot(T,Y(:,1));
title("x1(t)");
legend("predyktor-korektor","ode45")
xlabel("t");
ylabel("x1");
hold off;
figure;
hold on;
plot(t,x2v(1:length(t)));
plot(T,Y(:,2));
title("x2(t)");
legend("predyktor-korektor", "ode45");
xlabel("t");
ylabel("x2");
hold off;
figure;
hold on;
plot(x1v,x2v);
plot(Y(:,1),Y(:,2));
title("x2(x1)");
legend("predyktor-korektor", "ode45");
xlabel("x1");
ylabel("x2");
hold off;